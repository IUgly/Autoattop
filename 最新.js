(function () {
  function takeEvaluate() {
    return new Promise((resolve) => {
      var $list = $('.BT_ping').filter(function () {
        return !$(this).hasClass('Ped');
      })
      var len = $list.length;
      var now = 0;

      if (len === 0) {
        resolve();
      }

      function ping(i) {
        var frmBody = null;
        new Promise((resolve) => {
          $list.eq(i).trigger('click');
          goNextIf(() => {
            if (document.getElementById('pageiframe') && document.getElementById('pageiframe').contentWindow.document.querySelector('.ping_btn_3')) {
              frmBody = document.getElementById('pageiframe').contentWindow.document.body;
              return true;
            }
          }).then(() => {
            resolve();
          })
        }).then(() => {
          $('.ping_btn_3', frmBody).trigger('click');
          return goNextIf(() => {
            if (document.getElementById('pageiframe').contentWindow.document.querySelector('.aui_state_highlight')) {
              return true;
            }
          });
        }).then(() => {
          $('.aui_state_highlight', frmBody).trigger('click');
          return goNextIf(() => {
            if (!document.getElementById('pageiframe').contentWindow.document.querySelector('.ping_btn_3')) {
              return true;
            }
          });
        }).then(() => {
          $('.aui_close').trigger('click');
          if (now < len - 1) {
            now += 1;
            ping(now);
          } else {
            resolve();
          }
        })
      }
      ping(now);
    })
  }

  function getAnwsers() {
    [].slice.call($('.un-ans')).forEach(function (el) {
      var qus = el.getElementsByTagName('p')[0].innerText.slice(0, -3),
        qusTextIndex = a.indexOf(qus),
        anwsers = el.getElementsByTagName('li');

      [].slice.call(anwsers).forEach(function (el) {
        var ansTextIndex = a.indexOf(el.innerText, qusTextIndex);
        if (a[ansTextIndex - 1] === '√') {
          $(el.getElementsByTagName('input')[0]).trigger('click');
        }
      })
    })
    $('.btn100_org').eq(1).trigger('click');
    $('.aui_state_highlight').eq(0).trigger('click');
    window.location.reload();
  }

  function goNextIf(condition) {
    return new Promise((resolve, reject) => {
      var i = 0;
      var timer = setInterval(() => {
        if (condition()) {
          clearInterval(timer);
          resolve();
        }
        i += 1;
        if (i > 800) {
          clearInterval(timer);
          reject();
        }
      }, 10)
    })
  }

  takeEvaluate().then(getAnwsers);

})();
